    void GuestID_AI() {
        try {
            Connection con = DbConnection.getConnection();
            PreparedStatement stmt = con.prepareStatement("SELECT MAX(GuestID) FROM guest");
            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                jGuestID.setText(String.valueOf(rs.getInt(1) + 1));
            } else {
                jGuestID.setText("1");
            }
        } catch (Exception ex) {
            Logger.getLogger(GuestRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
