    private void jSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSubmitActionPerformed
        try {
            boolean hasError = false;

            // Reset background color when typing
            JTextField[] fields = {
                jGuestID, jFName, jLName, jMName,
                jAge, jContact, jAddress
            };

            DocumentListener resetListener = new DocumentListener() {
                @Override
                public void insertUpdate(DocumentEvent e) {
                    resetField(e);
                }

                @Override
                public void removeUpdate(DocumentEvent e) {
                    resetField(e);
                }

                @Override
                public void changedUpdate(DocumentEvent e) {
                    resetField(e);
                }

                private void resetField(DocumentEvent e) {
                    JTextField field = (JTextField) e.getDocument().getProperty("owner");

                    // Only reset field background if it now has text
                    if (!field.getText().trim().isEmpty()) {
                        field.setBackground(Color.WHITE);
                    }
                }
            };

            // Attach listener to each text field
            for (JTextField field : fields) {
                field.getDocument().putProperty("owner", field);
                field.getDocument().addDocumentListener(resetListener);
            }

            // Validate each text field
            for (JTextField field : fields) {
                if (field.getText().trim().isEmpty()) {
                    field.setBackground(Color.RED);
                    hasError = true;
                }
            }
            
                // Check if all fields have information
                if (jFName.getText().trim().isEmpty()
                        || jLName.getText().trim().isEmpty()
                        || jMName.getText().trim().isEmpty()
                        || jAge.getText().trim().isEmpty()
                        || jContact.getText().trim().isEmpty()
                        || jAddress.getText().trim().isEmpty()
                        || (!jRadioButton1.isSelected()
                        && !jRadioButton2.isSelected()
                        && !jRadioButton3.isSelected())) {

                    JOptionPane.showMessageDialog(this, "All fields must be filled!", "", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                // Name Validation
                if (!jFName.getText().matches("[A-Za-z]+")) {
                    JOptionPane.showMessageDialog(this, "First Name must contain letters only!", "", JOptionPane.ERROR_MESSAGE);
                    jFName.setBackground(Color.RED);
                    return;
                }

                if (!jLName.getText().matches("[A-Za-z]+")) {
                    JOptionPane.showMessageDialog(this, "Last Name must contain letters only!", "", JOptionPane.ERROR_MESSAGE);
                    jLName.setBackground(Color.RED);
                    return;
                }

                if (!jMName.getText().matches("[A-Za-z]")) {
                    JOptionPane.showMessageDialog(this, "Middle Name must be exactly 1 letter!", "", JOptionPane.ERROR_MESSAGE);
                    jMName.setBackground(Color.RED);
                    return;
                }
                // Age Validation
                int age;
                try {
                    age = Integer.parseInt(jAge.getText());
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Age must be a number!", "", JOptionPane.ERROR_MESSAGE);
                    jAge.setBackground(Color.RED);
                    return;
                }
                // Contact validation                
                if (!jContact.getText().matches("0\\d{10}")) {
                    JOptionPane.showMessageDialog(this, "Contact must start with 0 and be exactly 11 digits!", "", JOptionPane.ERROR_MESSAGE);
                    jContact.setBackground(Color.RED);
                    return;
                }
                int guestID = Integer.parseInt(jGuestID.getText());
                // Insert Database
                Connection con = DbConnection.getConnection();
                PreparedStatement smt = con.prepareStatement("INSERT INTO guest (GuestID,FName,LName,MName,Suffix,Age,Gender,Contact,Address)VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)");

                smt.setInt(1, guestID);
                smt.setString(2, jFName.getText());
                smt.setString(3, jLName.getText());
                smt.setString(4, jMName.getText());
                smt.setString(5, jSuffix.getText());
                smt.setInt(6, age);

                if (jRadioButton1.isSelected()) {
                    smt.setString(7, jRadioButton1.getText());
                } else if (jRadioButton2.isSelected()) {
                    smt.setString(7, jRadioButton2.getText());
                } else {
                    smt.setString(7, jRadioButton3.getText());
                }
                smt.setString(8, jContact.getText());
                smt.setString(9, jAddress.getText());

                smt.executeUpdate();

                JOptionPane.showMessageDialog(this, "Successfully created guest account!");

                ClearData();
                GuestID_AI();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "SQL Error: " + e.getMessage());
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Driver not found.");
            }
    }//GEN-LAST:event_jSubmitActionPerformed

